package com.exam.myapp;

public class MyVo {
	
	// 파라미터 값을 받기 위한 변수 선언
	private int x;
	private int y;
	
	// 변수이름과 파라미터이름이 같아야한다
	
	public int getX() {	//속성 이름
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}

	

}
